package fr.byxis.fireland.utilities;

public class Vector2 {

    private double x;
    private double y;

    public Vector2(double _x, double _y)
    {
        this.x = _x;
        this.y = _y;
    }

    public double getX()
    {
        return x;
    }

    public void setX(double _x)
    {
        this.x = _x;
    }

    public double getY()
    {
        return y;
    }

    public void setY(double _y)
    {
        this.y = _y;
    }
}
